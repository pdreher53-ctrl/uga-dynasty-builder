// Get a rival school for recruiting battle scenarios
export function getRandomRival(): SECSchool {
  const rivals = secSchools.filter((s) => s.id !== 'uga');
  return rivals[Math.floor(Math.random() * rivals.length)];
}

// Get schools by rivalry intensity
export function getSchoolsByRivalry(level: SECSchool['rivalryWithUGA']): SECSchool[] {
  return secSchools.filter((s) => s.rivalryWithUGA === level && s.id !== 'uga');
}

